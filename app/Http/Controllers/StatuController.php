     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'nom' => 'required',
        ]);

        $status = new Statu([
            'nom' => $request->input('nom'),
        ]);
        $status->save();
        return redirect('/dashboard')->with('message', 'Le status a bien été créée !');
    }

